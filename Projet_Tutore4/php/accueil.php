            <h2>Contexte</h2>
            <p>Depuis la mise en place des projets tutorés, un certain nombre de dispositifs de télécommunication ont été mis en place, notamment la réception de cartes météo depuis les satellites NOAA ou les images envoyées depuis l’ISS. On se propose dans ce projet de réaliser un serveur Web sur lequel nous pourrions réceptionner les différentes images météo reçues par le système de réception météo. Le projet est constitué de 3 parties : </p>
            <br>
            <p>- Mise en place du serveur Web</p>
            <p>- Rédaction d’un tutoriel pour la mise à jour du serveur Web (modification des informations de manière générale)</p>
            <p>- Ajout d’information sur le site au choix (température, pluviométrie, etc...)</p>
